

fun main()
{
    // break & continue used in for
    for (i in 1..10){
        if (i==5)
            break
            //continue
        println(i)
    }

    // break & continue used in while
    var j = 0
    while (j<=10){
        if (j==5)
            break
        println(j)
        j++
    }

    
}